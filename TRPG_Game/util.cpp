int menuDraw() {
    int x = 50;
    int y = 12;
    gotoxy(x - 2, y);
    std::cout << "> 게임시작   ";
    gotoxy(x, y + 1);
    std::cout << "게임정보  ";
    gotoxy(x, y + 2);
    std::cout << "  종료  ";

    while (true) {
        int n = keyControl();
        switch (n) {
        case UP: {
            if (y > 12) {
                gotoxy(x - 2, y);
                std::cout << " ";
                gotoxy(x - 2, --y);
                std::cout << ">";
            }
            break;
        }

        case DOWN: {
            if (y < 14) {
                gotoxy(x - 2, y);
                std::cout << " ";
                gotoxy(x - 2, ++y);
                std::cout << ">";
            }
            break;
        }

        case SUBMIT: {
            return y - 12; // 선택한 메뉴의 인덱스를 반환합니다.
        }
        default:
            break;
        }
    }
}


